public class Node {
    private int nodeId;
    private String nodeAddr;
    private int port;
    public int getNodeId() {
        return nodeId;
    }
    public void setNodeId(int nodeId) {
        this.nodeId = nodeId;
    }
    public String getNodeAddr() {
        return nodeAddr;
    }
    public void setNodeAddr(String nodeAddr) {
        this.nodeAddr = nodeAddr;
    }
    public int getPort() {
        return port;
    }
    public void setPort(int port) {
        this.port = port;
    }

}

